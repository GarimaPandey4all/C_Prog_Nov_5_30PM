#include<stdio.h>
#include<conio.h>
/*
      Pre Processor Directives

      #--> Pre Processor
      include--> Directive--> Directory --> folder

      stdio.h --> Standard Input / Output header file (printf())
      conio.h --> Console Input / Output header file (clrscr(), getch())

*/

void main() // main() - Entry Point; ()-> parenthesis
{
   clrscr(); //clear screen
   printf("Hello World"); // print output on the output/console screen
   getch(); //Get Character
}